<template>
<div v-if="reports">
  <h1>Verslagen</h1>
  <div class="container my-2" v-for="value in reports" :key="value.id">
    <button class="btn btn-secondary btn-lg btn-block" v-on:click="reportClick(String(value.id))">
      {{
        new Date(value.date).toLocaleString([], {
          year: "numeric",
          month: "numeric",
          day: "numeric",
          hour: "2-digit",
          minute: "2-digit",
          hour12: false
        })
      }}
      <span
        class="badge badge-primary ml-3"
      >{{ getShift(value.nightShift) }}</span>
    </button>
  </div>
</div>
</template>

<script lang="ts">
import Vue from "vue";
import ReportingService from "../services/ReportingService";
export default Vue.extend({
  data: function () {
    return {
      reports: [] as any[],
    };
  },

  mounted() {
    this.loadData();
  },

  methods: {
    loadData: function () {
      ReportingService.getAllReports("/api/reports/all").then(
        res => (this.reports = res)
      );
    },

    reportClick: function (id: string) {
      this.$router.push({
        path: "reportView",
        query: {
          reportId: id
        }
      });
    },

    getShift: function(nightShift: boolean) {
      if(nightShift)
        return "Nachtshift â˜¾";
      
      return "Dagshift ðŸŒ£";
    }
        
  }
});
</script>
